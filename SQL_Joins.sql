--INNER JOIN
SELECT E.NAME,E.BRANCH,E.GENDER,B.NAME,E.BRANCHID
FROM  EMP E
INNER JOIN BRANCH B 
ON E.ID = B.ID


--LEFT JOIN
SELECT E.NAME,E.BRANCH,E.GENDER,B.ID,E.BRANCHID
FROM  EMP E 
LEFT JOIN BRANCH B
ON E.ID = B.ID

--RIGHT JOIN
SELECT E.NAME,E.BRANCH,E.BRANCHID,E.GENDER,B.NAME
FROM EMP E  
RIGHT JOIN BRANCH B
ON E.ID = B.ID

--FULL JOIN 
SELECT E.NAME,E.BRANCH,E.BRANCHID,E.GENDER, B.NAME
FROM EMP E
FULL JOIN BRANCH B
ON E.ID = B.ID

--CROSS JOIN
SELECT E.NAME,E.BRANCH,E.BRANCHID,E.GENDER, B.NAME
FROM EMP E
CROSS JOIN BRANCH B

