---JOINS : JOINS ARE USED TO COMBINE 2 OR MORE TABLE 
-- TYPES OF JOINS ARE
-- INNER JOIN : RETURNS MATCHING RECORDS FROM TWO OR MORE TABLES.
-- LEFT JOIN : RETURNS ALL RECORDS FROM LEFT TABLE AND MATCHING VALUE FROM 2 OR MORE TABLE.
-- RIGHT JOIN : RETURNS ALL RECORDS FROM RIGHT TABLE AND MATCHING VALUE FROM 2 OR MORE TABLE.
-- FULL JOIN : RETURNS ALL THE RECORDS FROM BOTH TABLES MATCHING AS WELL AS NON MATCHING.
-- CROSS JOIN : COMBINES EACH ROW FROM FIRST TABLE WITH EACH ROW FROM SECOND TABLE.

