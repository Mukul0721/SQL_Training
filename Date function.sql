--DATE FUNCTIONS
GETDATE : IT WILL RETURN CURRENT DATE WITH TIME

SELECT GETDATE()

DATEPART : IT WILL RETURN PART OF DATE OR TIME
 SELECT DATEPART(DAY,GETDATE()) AS CURRENTDATE
 SELECT DATEPART(MONTH,GETDATE()) AS CURRENTMONTH
 SELECT DATEPART(YEAR,GETDATE()) AS CURRENTYEAR

 DATEADD : IT WILL ADD DATE
 DATEADD(DATEPART, NUMBER ,DATECOLUMNNAME)
 SELECT DATEADD(YEAR, 10, GETDATE())

 DATEDIFF : RETURNS TIME BETWEEN TWO DATES
 SELECT DATEDIFF(MINUTE,'2022-12-12','2022-11-11')

 CONVERT : IT WILL DISPLAYS DATE AND TIME DATA IN DIFFERENT FORMATS.
 --SELECT CONVERT (DATATYPE, EXPRESSION, STYLE) SYNTAX
   SELECT CONVERT(VARCHAR(20),getdate())
   SELECT CONVERT(VARCHAR(20),GETDATE(),10)

    
